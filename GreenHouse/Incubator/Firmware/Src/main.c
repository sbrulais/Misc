/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	  processClock = 0;

	  // Set clock here !
	  sec = 55;
	  min = 59;
	  hour = 6;

	  pwrV = 0;
	  pwrB = 0;
	  pwrR = 0;
	  pwrW = 0;

	  tRatio = 0.;

	  // Set gRatio here !
	  gRatio = .8;

	  ledPulse = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  mLED_OFF;


  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

  PWM_REG_V = pwrV;
  PWM_REG_B = pwrB;
  PWM_REG_R = pwrR;
  PWM_REG_W = pwrW;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	 if(processClock)
	 {
	   processClock = 0;
	   if(++sec == 60)
	   {
		  sec = 0;
		  if(++min == 60)
		  {
			 min = 0;
			 if(++hour == 24)
			 {
				hour = 0;
			 }
		  }
		  // Update led power
		  // 0h-6h : OFF
		  // 6h-7h : increment 0-100%
		  // 8h-20h : 100%
		  // 20h-21h : decrement 100-0%
		  // 21h-0h : OFF
		  if(hour < 6) tRatio = 0;
		  else if(hour < 7) tRatio = (float)min/60.;
		  else if(hour < 20) tRatio = 1.;
		  else if(hour < 21) tRatio = (60. - (float)min)/60.;
		  else tRatio = 0;

		  pwrV = (int)((float)BASE_PWR_V * tRatio * gRatio);
		  pwrB = (int)((float)BASE_PWR_B * tRatio * gRatio);
		  pwrR = (int)((float)BASE_PWR_R * tRatio * gRatio);
		  pwrW = (int)((float)BASE_PWR_W * tRatio * gRatio);

		  if(pwrV > 100) pwrV = 0;
		  if(pwrR > 100) pwrR = 0;
		  if(pwrB > 100) pwrB = 0;
		  if(pwrW > 100) pwrW = 0;

		  PWM_REG_V = pwrV;
		  PWM_REG_B = pwrB;
		  PWM_REG_R = pwrR;
		  PWM_REG_W = pwrW;
	   }
	   // LED output
	   if((sec % 10) == 0)
	   {
		   ledPulse = hour * 2;
	   }
	 }

  }
  /* USER CODE END 3 */
}

